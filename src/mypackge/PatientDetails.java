package mypackge;

/**
 *
 * @author Pasindu Weerasinghe
 */
public class PatientDetails {
    private String name;
    private int age;
    private String sex;
    private String address;
    private String contactno;
    private int wardno;
    private int docdid;
    private int docempid;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the sex
     */
    public String getSex() {
        return sex;
    }

    /**
     * @param sex the sex to set
     */
    public void setSex(String sex) {
        this.sex = sex;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the contactno
     */
    public String getContactno() {
        return contactno;
    }

    /**
     * @param contactno the contactno to set
     */
    public void setContactno(String contactno) {
        this.contactno = contactno;
    }

    /**
     * @return the wardno
     */
    public int getWardno() {
        return wardno;
    }

    /**
     * @param wardno the wardno to set
     */
    public void setWardno(int wardno) {
        this.wardno = wardno;
    }

    /**
     * @return the docdid
     */
    public int getDocdid() {
        return docdid;
    }

    /**
     * @param docdid the docdid to set
     */
    public void setDocdid(int docdid) {
        this.docdid = docdid;
    }

    /**
     * @return the docempid
     */
    public int getDocempid() {
        return docempid;
    }

    /**
     * @param docempid the docempid to set
     */
    public void setDocempid(int docempid) {
        this.docempid = docempid;
    }
           
}
